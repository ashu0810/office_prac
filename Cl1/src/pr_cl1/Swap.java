package pr_cl1;

public class Swap {

	
	public static void main(String[] args) {
System.out.println("Before Swapping");
int x= 40;
int y= 30;
int z;
System.out.println("Value of x " +x);
System.out.println("Value of y " +y);
System.out.println("After Swapping");
z=x;
x=y;
y=z;


System.out.println("Value of y " +y);
System.out.println("Value of x " +x);


		
		
	}
}
